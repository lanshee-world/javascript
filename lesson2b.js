// Comparison Operators
// They usually evaluate to a boolean answer
let number1 = 5
let number2 = 10

console.log(number1 == number2)
console.log(number1 >= number2)
console.log(number1 <= number2)
console.log(number1 > number2)
console.log(number1 < number2)
console.log(number1 != number2)





// triple equal sign(===) - research in javascript
// Used to compare both the value and the data type
let num1 = 5
let num2 = "5"
console.log(num1 === num2) // will return false since the data types are different




// Logical Operators:Are used to evaluate two or more conditions and they also give a boolean answer
// Logical AND (&&)- it evaluates to true if and only if all of the conditions/statements are true.
console.log((number1 < number2) && (number2 > number1)) 

// Logical OR (||) - it evaluates to true if one of the conditions is true.
console.log((number1 > number2) || (number2 > number1))

// Logical NOT (!) - It is used to negate a statement/condition
console.log(!(number1 < number2))




